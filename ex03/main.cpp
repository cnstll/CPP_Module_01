#include "HumanA.hpp"
#include "HumanB.hpp"
#include <iostream>
#include <string>

int main()
{
	{
		Weapon club = Weapon("a crude spiked club");
		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	{
		Weapon club = Weapon("a crude spiked club");
		HumanB jim("Jim");
		jim.attack();
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}
}
